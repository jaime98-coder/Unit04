package exercises02;

public class CuentaCorriente {

	String DNI;
	String nombre;
	Double saldo;
	String nacionalidad;

	public CuentaCorriente(String DNI, String nombre, Double saldo, String nacionalidad) {
		super();
		this.DNI = DNI;
		this.nombre = nombre;
		this.saldo = saldo;
		this.nacionalidad = nacionalidad;
	}
}
